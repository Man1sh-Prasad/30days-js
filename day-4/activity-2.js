/// activity-2: while loop

// task-3: write a program to calculate the sum of numbers from 1 to 10 using a while loop

let i = 1
let sum = 0
while(i <= 10) {
    sum += i
    i++
}
console.log(sum)

// task-4: write a program to print number from 10 to 1 using a while loop

let j = 10
while(j >= 1) {
    console.log(j)
    j--
}